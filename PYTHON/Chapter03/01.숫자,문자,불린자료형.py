# 숫자 자료형
# 1. 정수형 : 소수점이 없는 수
print(1)
print(3)
print(0)
print(-1)
print(1, 3, 0, -1)
# 공백 문자 없애고 싶은때 
print(1,3,0,-1,sep="")

# 2. 실수형 : 소수점이 있는 수
print(1.1, 3.1, 0, -1.4)

# 문자열 자료형
# "" or ''
print("파이썬 너무 재밌다", end='') #뒷문장 줄바꿈 안하고 바로 출력
print('파이썬 너무 재밌다')
print('"개 짖는 소리 좀 안나게 해라"라고 그는 외쳤다')

# 불린형
# True or False
print(True)
